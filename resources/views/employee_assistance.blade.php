                    <h4><b>What are some employee assistance program benefits for employers?</b> </h4><br>
                    Increased productivity and retention, lower insurance costs, and more.
                    <ol>
                        <li>• Increased productivity. Workers with depressive symptoms are more likely to be absent due to
                            illness or accidents, and even when they are at work, they can be checked out (a phenomenon
                            known as presenteeism). EAPs can give these workers the help they need so they can be back at
                            full productivity.</li>
                        <li>• Greater retention. Eighty-six percent of employees now believe a company’s culture should
                            support mental health. It’s a make-or-break issue for many, especially younger workers. An EAP
                            is a signal that you’re listening and that you care, which—in turn—can aid recruiting and
                            retention efforts.</li>
                        <li>• Lower insurance costs. Just as employer-sponsored biometric screenings can detect early signs
                            of disease, EAPs can also catch the beginnings of larger issues that would have ended up costing
                            you a lot more down the line.</li>
