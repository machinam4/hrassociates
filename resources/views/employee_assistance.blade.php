                    <h3><b>What is an Employee Assistance Programme (EAP)</b></h3>
                    An employee assistance program (EAP) is an employee benefit program that assists employees with personal
                    problems and/or work-related problems that may impact their job performance, For workers, employee
                    assistance programs can be a much-needed outlet to seek help. But there are benefits to employers who
                    implement employee assistance programs too.<br>
                    Services offered under EAP assist in resolving conflict, reducing workplace risks and enhancing
                    capability and resilience in individuals and teams.<br>
                    Besides improving employee productivity and decreasing sick days and leaves of absence, all of which
                    reduce operating costs, wellness programs may lower an organization’s health insurance expenses.
                    Employees can also benefit from wellness programs through lower health insurance premiums, decreased
                    out-of-pocket medical expenditures and an increased sense of well-being.<br>
                    Corporate wellness programs offer a variety of scheduled programs, such as stress management seminars
                    that address everything from sleep to work-life balance to financial health; cooking classes; healthy
                    recipe exchanges and fitness challenges. <br>
                    An employer approach to improving employee health, wellness programs include activities such as
                    company-sponsored exercise, weight-loss competitions, educational seminars, tobacco-cessation programs
                    and health screenings that are designed to help employees eat better, lose weight and improve their
                    overall physical health.<br>
